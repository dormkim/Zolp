    public void InputExcel(){

        Workbook wb = null;

        try {
            InputStream is = getAssets().open("result.xls");
            wb = Workbook.getWorkbook(is);
        } catch (IOException e) {
            e.printStackTrace();
        } catch (BiffException e) {
            e.printStackTrace();
        }

        if(wb != null) {
            Sheet sheet = wb.getSheet(0);   // 시트 불러오기
            if (sheet != null) {
                int colTotal = sheet.getColumns();    // 전체 컬럼
                int rowIndexStart = 1;                  // row 인덱스 시작
                rowTotal = sheet.getColumn(colTotal - 1).length;

                mapInfo = new double[rowTotal][2];
                timeList = new String[rowTotal];

                for (int row = rowIndexStart; row < rowTotal; row++) {
                    timeList[row] = sheet.getCell(0, row).getContents();
                    mapInfo[row][0] = Double.valueOf(sheet.getCell(1,row).getContents());
                    mapInfo[row][1] = Double.valueOf(sheet.getCell(2, row).getContents());
                }
            }
        }
    }

    public void setLocation(){
        polyline = new MapPolyline();
        polyline.setTag(1000);
        polyline.setLineColor(Color.argb(255, 0, 0, 0)); // Polyline 컬러 지정.

        for(int i = 1; i < rowTotal; i++){
            polyline.addPoint(MapPoint.mapPointWithGeoCoord(mapInfo[i][0], mapInfo[i][1]));
            MapPoint mapPoint = MapPoint.mapPointWithGeoCoord(mapInfo[i][0], mapInfo[i][1]);

            MapPOIItem marker = new MapPOIItem();
            marker.setItemName("시간" + timeList[i]);
            marker.setTag(0);
            marker.setMapPoint(mapPoint);
            // 기본으로 제공하는 BluePin 마커 모양.
            marker.setMarkerType(MapPOIItem.MarkerType.BluePin);
            mMapView.addPOIItem(marker);
        }

        mMapView.addPolyline(polyline);

        // 지도뷰의 중심좌표와 줌레벨을 Polyline이 모두 나오도록 조정.
        MapPointBounds mapPointBounds = new MapPointBounds(polyline.getMapPoints());
        int padding = 100; // px
        mMapView.moveCamera(CameraUpdateFactory.newMapPointBounds(mapPointBounds, padding));
    }

